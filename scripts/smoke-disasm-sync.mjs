#!/usr/bin/env node
// Spec 249 smoke — disasm sync, runtime table discovery, annotation suggestions.
//
// Cases:
//  1. parseAsmFile — extracts labels from a synthetic .asm correctly
//  2. emitToAsmFile — adds auto-label at correct address, preserves human comment
//  3. round-trip — human comments survive emit + re-parse unaltered
//  4. runtime-table-discovery — synthetic Y-indexed reads → pointer table found
//  5. suggestion routing — fingerprint match ≥0.95 → autoWrite; 0.7 → openQuestion
//  6. ground-truth guard — existing label at address blocks re-suggestion
//
// All cases must PASS for smoke to exit 0.

import { mkdirSync, writeFileSync, existsSync, readFileSync, rmSync } from "node:fs";
import { resolve as resolvePath } from "node:path";

const repoRoot = resolvePath(import.meta.dirname, "..");
const tmpDir = "/tmp/c64re-disasm-sync-smoke";

if (existsSync(tmpDir)) rmSync(tmpDir, { recursive: true, force: true });
mkdirSync(tmpDir, { recursive: true });

// ---- Load pipeline modules (CommonJS .cjs via dist/) -------------------

const distPipeline = `${repoRoot}/dist/pipeline`;

// The pipeline compiles to CommonJS .cjs files. Use createRequire.
import { createRequire } from "node:module";
const require = createRequire(import.meta.url);

let asmSync, runtimeTables, annotationSuggestions;
try {
  asmSync = require(`${distPipeline}/disasm/asm-sync.cjs`);
  runtimeTables = require(`${distPipeline}/analysis/runtime-tables.cjs`);
  annotationSuggestions = require(`${distPipeline}/analysis/annotation-suggestions.cjs`);
} catch (err) {
  console.error("FAIL: could not load pipeline modules:", err.message);
  console.error("Hint: run 'npm run build:pipeline' first");
  process.exit(1);
}

const { parseAsmFile, emitToAsmFile } = asmSync;
const { analyzeRuntimeTables } = runtimeTables;
const { generateAnnotationSuggestions, mergeAutoWriteSuggestions } = annotationSuggestions;

// ---- Test harness -------------------------------------------------------

let passed = 0;
let failed = 0;

function assert(condition, msg) {
  if (condition) {
    console.log(`  PASS: ${msg}`);
    passed++;
  } else {
    console.error(`  FAIL: ${msg}`);
    failed++;
  }
}

// ---- Case 1: parseAsmFile — label extraction ----------------------------

console.log("\nCase 1: parseAsmFile — label extraction");

const asmContent = [
  `; Autogenerated disassembly`,
  ``,
  `main_loop:                         ; $0812  Loop entry`,
  `        LDA $D012                  ; $0812  read raster`,
  `        BNE $0812                  ; $0814`,
  `irq_handler:                       ; $0816  IRQ entry`,
  `        PHA                        ; $0816`,
  `        RTS                        ; $0817`,
  ``,
].join("\n");

const asmPath1 = `${tmpDir}/test1_disasm.asm`;
writeFileSync(asmPath1, asmContent, "utf8");

const ast1 = parseAsmFile(asmPath1);
assert(ast1.labels.has(0x0812), "label 'main_loop' at $0812 found");
assert(ast1.labels.get(0x0812)?.name === "main_loop", "label name is 'main_loop'");
assert(ast1.labels.has(0x0816), "label 'irq_handler' at $0816 found");
assert(ast1.labels.size === 2, `label count == 2 (got ${ast1.labels.size})`);

// ---- Case 2: emitToAsmFile — new auto-label insertion ------------------

console.log("\nCase 2: emitToAsmFile — auto-label at new address");

const asmContent2 = [
  `; Disassembly`,
  `        LDA $20                    ; $0820`,
  `        STA $D800                  ; $0822`,
  `        RTS                        ; $0824`,
  ``,
].join("\n");

const asmPath2 = `${tmpDir}/test2_disasm.asm`;
writeFileSync(asmPath2, asmContent2, "utf8");

const additions = {
  newLabels: new Map([[0x0822, "_auto_label_0822"]]),
  newComments: new Map(),
  newRoutineDocs: [],
};

const written = emitToAsmFile(asmPath2, additions);
assert(written > 0, `emitToAsmFile wrote ${written} line(s)`);

const patched2 = readFileSync(asmPath2, "utf8");
assert(patched2.includes("_auto_label_0822"), "auto-label present in patched file");
assert(patched2.includes("$0822"), "address anchor still present");

// ---- Case 3: round-trip — human comments survive -----------------------

console.log("\nCase 3: round-trip — human comments preserved");

const humanComment = "human-authored: do not delete";
const asmContent3 = [
  `; ${humanComment}`,
  `entry:                             ; $0900`,
  `        LDA #$00                   ; $0900  init`,
  `        RTS                        ; $0902`,
  ``,
].join("\n");

const asmPath3 = `${tmpDir}/test3_disasm.asm`;
writeFileSync(asmPath3, asmContent3, "utf8");

// Emit something unrelated (new label at $0904 which doesn't exist — no-op)
const additions3 = {
  newLabels: new Map([[0x0910, "_auto_label_0910"]]),
  newComments: new Map(),
  newRoutineDocs: [],
};
emitToAsmFile(asmPath3, additions3);

const round3 = readFileSync(asmPath3, "utf8");
assert(round3.includes(humanComment), "human comment survived round-trip");
assert(round3.includes("entry:"), "original label 'entry:' preserved");

// ---- Case 4: runtime table discovery -----------------------------------

console.log("\nCase 4: runtime-table-discovery — pointer table from Y-indexed reads");

// Simulate reading a 4-word pointer table at $1000-$1007
// Consumer PC $0830 uses abs,Y opcode 0xB9 (LDA abs,Y)
const memReads = [];
for (let y = 0; y < 8; y++) {
  memReads.push({ cycle: 1000 + y * 4, pc: 0x0830, addr: 0x1000 + y, value: 0x20 + y, y });
}

const cpuSteps = [
  { cycle: 1000, pc: 0x0830, opcode: 0xB9, y: 0 },
];

const tables = analyzeRuntimeTables("art_001", { memReads, cpuSteps });
assert(tables.length >= 1, `discovered ${tables.length} table(s) (expected ≥1)`);
if (tables.length > 0) {
  const tbl = tables[0];
  assert(tbl.range[0] === 0x1000, `table starts at $1000 (got $${tbl.range[0].toString(16)})`);
  assert(tbl.artifactId === "art_001", "artifactId propagated");
  assert(tbl.evidence.accessCount >= 8, `accessCount ≥8 (got ${tbl.evidence.accessCount})`);
}

// ---- Case 5: suggestion routing by confidence --------------------------

console.log("\nCase 5: suggestion routing — fingerprint ≥0.95 → autoWrite, 0.7 → OQ");

const input5 = {
  artifactId: "art_002",
  fingerprintMatches: [
    { address: 0xC000, libraryName: "kernal_load", confidence: 0.97, description: "KERNAL load routine" },
    { address: 0xC100, libraryName: "sid_init",    confidence: 0.72, description: "SID init" },
    { address: 0xC200, libraryName: "weak_match",  confidence: 0.40, description: "weak" },
  ],
};

const result5 = generateAnnotationSuggestions(input5);
assert(result5.autoWrite.length === 1, `autoWrite has 1 entry (got ${result5.autoWrite.length})`);
assert(result5.openQuestions.length === 1, `openQuestions has 1 entry (got ${result5.openQuestions.length})`);
assert(result5.logOnly.length === 1, `logOnly has 1 entry (got ${result5.logOnly.length})`);
assert(
  result5.autoWrite[0].annotation.address === "C000",
  `autoWrite[0] is C000 (got ${result5.autoWrite[0].annotation.address})`
);

// ---- Case 6: ground-truth guard ----------------------------------------

console.log("\nCase 6: ground-truth guard — existing label blocks re-suggestion");

const input6 = {
  artifactId: "art_003",
  groundTruthLabels: new Set([0xC000]),  // human label at C000
  fingerprintMatches: [
    { address: 0xC000, libraryName: "kernal_load", confidence: 0.99 },  // should be blocked
    { address: 0xD000, libraryName: "some_other",  confidence: 0.98 },  // should pass
  ],
};

const result6 = generateAnnotationSuggestions(input6);
const hasC000 = result6.autoWrite.some(s => s.annotation.address === "C000");
const hasD000 = result6.autoWrite.some(s => s.annotation.address === "D000");
assert(!hasC000, "C000 blocked by ground-truth guard");
assert(hasD000, "D000 passes (no ground-truth label there)");

// ---- Summary ------------------------------------------------------------

console.log(`\n${"=".repeat(50)}`);
console.log(`Spec 249 smoke: ${passed} PASS  ${failed} FAIL`);
if (failed > 0) {
  process.exit(1);
}
